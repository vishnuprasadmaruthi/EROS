    private void processjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = statusjTable.getSelectedRow();

        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) statusjTable.getValueAt(selectedRow, 0);
            if (request.getStatus().equalsIgnoreCase("Completed")) {
                JOptionPane.showMessageDialog(null, "Request already processed.");

            } else {
                request.setReceiver(userAccount);
                request.setStatus("Processing");
                patientStatus();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Choose a request to process.");

        }
    }//GEN-LAST:event_processjButtonActionPerformed

    private void sendReportjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendReportjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = statusjTable.getSelectedRow();

        if (selectedRow >= 0) {
            PatientStatusWorkRequest pswr = (PatientStatusWorkRequest) statusjTable.getValueAt(selectedRow, 0);
            pswr.setReceiver(userAccount);
            pswr.setStatus("Completed");
            pswr.setBloodPressure(Float.parseFloat(BPjTextField.getText()));
            pswr.setTemperature(Float.parseFloat(temperaturejTextField.getText()));
            pswr.setWeight(Float.parseFloat(weightjTextField.getText()));
            pswr.setInjury(txtInjury.getText());

            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof DoctorOrganization) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(pswr);
                userAccount.getWorkQueue().getWorkRequestList().add(pswr);
            }

            JOptionPane.showMessageDialog(this, "Patient report sent successfully!!");
        }
    }//GEN-LAST:event_sendReportjButtonActionPerformed

