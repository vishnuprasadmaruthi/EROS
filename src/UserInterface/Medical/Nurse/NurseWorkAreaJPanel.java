    public void patientComboBox() {
        patientjComboBox.removeAllItems();
        for (Patient p : organization.getPatientDirectory().getPatientDirectory()) {
            patientjComboBox.addItem(p);
        }
    }

    public void doctorComboBox() {
        doctorjComboBox.removeAllItems();
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof DoctorOrganization) {
                for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                    doctorjComboBox.addItem(ua);
                }
            }
        }
    }

    public void patientStatus() {
        DefaultTableModel dtm = (DefaultTableModel) statusjTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[3];
            row[0] = request;
            row[1] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[2] = request.getStatus();

            dtm.addRow(row);
        }
    }

